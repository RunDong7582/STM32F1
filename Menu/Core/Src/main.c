void LoadUI(void)
{
    ssd1306_Init();
    if (hi2c1.Instance->SR2 & I2C_SR2_BUSY) {
    // I2C is busy, perform reset or re-initialization
    HAL_I2C_DeInit(&hi2c1);
    HAL_Delay(1); // Add a small delay to ensure deinitialization is complete
    MX_I2C1_Init();
    ssd1306_Init();
  }
    HAL_GPIO_TogglePin(LED_GPIO_Port,LED_Pin);
		ssd1306_SetCursor(12,20);
		ssd1306_WriteString("LED ON", Font_16x26, White);
    for(int i=0 ; i<12; i++)
	  {
			ssd1306_DrawArcWithRadiusLine(63, 32, 28, i*30, (i+1)*30, White);
			HAL_Delay(100);
			ssd1306_UpdateScreen();
		}
		HAL_Delay(500);
		ssd1306_Fill(Black);
		ssd1306_UpdateScreen();
}

void UpdateDisplayOnButtonPress(void)
{
    static uint8_t event_cnt = 0;
    if ( KEY.update ) {event_cnt = (event_cnt + 1)%2; KEY.update = 0;}
    switch (event_cnt)
    {
      case 0:
        KEY.Down = 1;
      break;
      case 1:
        KEY.Up =   1;
      break;
      case 2:
        KEY.Enter = 1;
      break;
      default:
      break;
    }
    
}

void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
    switch (GPIO_Pin)
    {
        case K1_Pin:
          if ( HAL_GPIO_ReadPin(K1_GPIO_Port, K1_Pin) == SET)
          {
              UpdateDisplayOnButtonPress(); 
          }
        break;
        default:
        break;
    }
}
